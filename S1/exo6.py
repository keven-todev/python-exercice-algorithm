# Recherche du plus grand nombre:
# Écrivez un programme qui prend trois nombres en entrée et affiche le plus grand.




nbr1 = float(input("Quel est votre premié nombre ?"))
nbr2 = float(input("Quel est votre deuxième nombre ?"))
nbr3 = float(input("Quel est votre troisième nombre ?"))



# max function can get the largest possible number of
theMax = max(nbr1, nbr2, nbr3)

print(theMax)

